def air_speed_of_sound(temp_c: float = AIR_TEMP_C) -> float:
    """공기 음속 [m/s]."""
    return 331.3 + 0.606 * temp_c


def air_density(temp_c: float = AIR_TEMP_C) -> float:
    """공기 밀도 [kg/m³]. 이상기체: ρ = P/(R_s·T)."""
    return 101325.0 / (287.05 * (273.15 + temp_c))
